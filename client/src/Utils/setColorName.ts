export const setColorName = (expression: string) => {
	switch (expression) {
		case "black":
			return "#404040";

		case "blue":
			return "#8080ff";

		case "red":
			return "#ff4d94";

		case "white":
			return "#f0f0f5";

		case "grey":
			return "#999999";

		case "rose":
			return "#ffe6ff";
	}
};
